#include "pch.h"
#include "Global.h"


  
  
 
#define CLT_VERSION 1720  //a remettre en 1711
#define SUB_VERSION "R.013 So ein Misthaufen!" 

#define BUILD_NUMBER __DATE__ " " __TIME__

       
      
// Sub versions:
// a : alpha
// b : beta
// g : release
        
   
//////////////////////////////////////////////////////////////////////////////////////////
#include "version.h"
#include <stdio.h>

namespace Version{

char versionBuffer[ 256 ] = { 0 };

//////////////////////////////////////////////////////////////////////////////////////////
const char *GetVersionText( void )
//////////////////////////////////////////////////////////////////////////////////////////
//  Returns the text version
//////////////////////////////////////////////////////////////////////////////////////////
{
    if( versionBuffer[ 0 ] == 0 ){
        sprintf_s( versionBuffer,256, "Version %.2f%s, build %s", ((float)CLT_VERSION)/1000, SUB_VERSION, BUILD_NUMBER );
    }

    return versionBuffer;
}
//////////////////////////////////////////////////////////////////////////////////////////
unsigned long GetVersion( void )
//////////////////////////////////////////////////////////////////////////////////////////
//  Returns the number version
//////////////////////////////////////////////////////////////////////////////////////////
{
    return CLT_VERSION;
}
//////////////////////////////////////////////////////////////////////////////////////////
const char* GetBuild( void )
//////////////////////////////////////////////////////////////////////////////////////////
//  Returns the build number.
//////////////////////////////////////////////////////////////////////////////////////////
{
    return BUILD_NUMBER;
}

//////////////////////////////////////////////////////////////////////////////////////////
char* GetSubVersion( void )
//////////////////////////////////////////////////////////////////////////////////////////
//  Returns the sub version character.
//////////////////////////////////////////////////////////////////////////////////////////
{
    return SUB_VERSION;
}

};

